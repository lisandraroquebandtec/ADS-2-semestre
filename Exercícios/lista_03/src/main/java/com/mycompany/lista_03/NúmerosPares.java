//Crie uma classe chamada “NúmerosPares” que:
//Exiba todos os números pares de 0 a 40 usando while.
public class NúmerosPares {
    public static void main(String[] args) {
        
       Integer numero= 2; 
       while(numero <= 40){
            System.out.println(numero);
            numero+=2;
        }
        
    }

}
